int		ft_strcmp(const char *s1, const char *s2)
{
	unsigned char	u1;
	unsigned char	u2;

	while (*s1 || *s2)
	{
		u1 = (unsigned char)*s1++;
		u2 = (unsigned char)*s2++;
		if (u1 != u2)
			return (u1 - u2);
		if (u1 == '\0')
			return (0);
	}
	return (0);
}
