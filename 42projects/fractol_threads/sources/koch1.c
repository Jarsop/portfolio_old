#include "../includes/fractol.h"

void	ft_init_koch(t_mlx *mlx, int o)
{
	mlx->x1 = 400;
	mlx->y1 = 75;
	mlx->x2 = 650;
	mlx->y2 = 425;
	if (o == 1)
	{
		mlx->x1 = 650;
		mlx->y1 = 425;
		mlx->x2 = 150;
		mlx->y2 = 425;
	}
	if (o == 2)
	{
		mlx->x1 = 150;
		mlx->y1 = 425;
		mlx->x2 = 400;
		mlx->y2 = 75;
	}
}

int		*ft_init_tkm(t_mlx *mlx, int *koch)
{
	koch[0] = mlx->x1;
	koch[1] = mlx->y1;
	koch[2] = mlx->x2;
	koch[3] = mlx->y2;
	return (0);
}

void	ft_init_tk(int **t, int *k)
{
	t[0][0] = k[0];
	t[0][1] = k[1];
	t[0][2] = (2 * k[0] + k[2]) / 3;
	t[0][3] = (2 * k[1] + k[3]) / 3;
	t[3][0] = (k[0] + 2 * k[2]) / 3;
	t[3][1] = (k[1] + 2 * k[3]) / 3;
	t[1][0] = t[0][2];
	t[1][1] = t[0][3];
	t[1][2] = t[0][2] + (t[3][0] - t[0][2]) /
		2 + (t[3][1] - t[0][3]) * SQRT_3 / 2;
	t[1][3] = t[0][3] - (t[3][0] - t[0][2]) *
		SQRT_3 / 2 + (t[3][1] - t[0][3]) / 2;
	t[2][0] = t[1][2];
	t[2][1] = t[1][3];
	t[2][2] = t[3][0];
	t[2][3] = t[3][1];
	t[3][2] = k[2];
	t[3][3] = k[3];
}

void	ft_init_mlxk(t_mlx *mlx)
{
	mlx->zoom = 250;
	mlx->x_o = 0;
	mlx->y_o = 0;
	mlx->iteration_max = 0;
}
